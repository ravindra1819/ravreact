// What are Modules ?

// Modules are seperate Javascript files that export and import code.
// They allow us to split a large program into smaller, manageable and reuseable pieces.
// Each files becomes an independent, reuseable unit of functionality.

// Why :
// Better code organization 
    // Each feature or UI section has its own logic and files.
// Reusability :
    // A single function or variable can be reused in multiple pages.
// Maintainability :
    // Easier to debug and modify without touching other files.
// Readability :
    // Smaller focused files -- > cleaner speration of concerns.


// Types of Exports :

    // Named Export :
        // Allows exporting multiple values from a module.
    // Default Export :
        // Used when a module exports only one main thing.

